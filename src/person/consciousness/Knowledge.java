package person.consciousness;


public class Knowledge {

    private int level;

    /**
     * Constructor with level of knowledge
     *
     * @param level
     */
    public Knowledge(int level) {

        this.level = level;
    }

    /**
     * getter for level of knowledge
     *
     * @return
     */
    public int getLevel() {
        return level;
    }
}
